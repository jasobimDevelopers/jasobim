package com.my.spring.model;
public class MechanicPricePojos {
	private String userName;
	private String workName;
	private Integer hourNum;
	private Float dayNum;
	private Integer daySalary;
	private Float salaryNum;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getWorkName() {
		return workName;
	}
	public void setWorkName(String workName) {
		this.workName = workName;
	}
	public Integer getHourNum() {
		return hourNum;
	}
	public void setHourNum(Integer hourNum) {
		this.hourNum = hourNum;
	}
	public Float getDayNum() {
		return dayNum;
	}
	public void setDayNum(Float dayNum) {
		this.dayNum = dayNum;
	}
	public Integer getDaySalary() {
		return daySalary;
	}
	public void setDaySalary(Integer daySalary) {
		this.daySalary = daySalary;
	}
	public Float getSalaryNum() {
		return salaryNum;
	}
	public void setSalaryNum(Float salaryNum) {
		this.salaryNum = salaryNum;
	}
	
	
}
