package com.my.spring.model;


public class AttenceForgetFactLogs {
	private Long user_id;
	private Integer fact_nums;
	private Integer forget_nums;
	public Long getUser_id() {
		return user_id;
	}
	public void setUser_id(Long user_id) {
		this.user_id = user_id;
	}
	public Integer getFact_nums() {
		return fact_nums;
	}
	public void setFact_nums(Integer fact_nums) {
		this.fact_nums = fact_nums;
	}
	public Integer getForget_nums() {
		return forget_nums;
	}
	public void setForget_nums(Integer forget_nums) {
		this.forget_nums = forget_nums;
	}
	
	
	
	
}
