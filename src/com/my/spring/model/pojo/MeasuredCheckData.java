package com.my.spring.model.pojo;

public class MeasuredCheckData {
	private Long measuredId;
	private Integer score;
	private Integer state;
	public Long getMeasuredId() {
		return measuredId;
	}
	public void setMeasuredId(Long measuredId) {
		this.measuredId = measuredId;
	}
	public Integer getScore() {
		return score;
	}
	public void setScore(Integer score) {
		this.score = score;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	
}
